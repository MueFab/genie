#include "ordered-section.h"

// ---------------------------------------------------------------------------------------------------------------------

namespace genie {
namespace util {

// ---------------------------------------------------------------------------------------------------------------------

OrderedSection::OrderedSection(OrderedLock* _lock, const Section& id) : lock(_lock), length(id.length) {
    lock->wait(id.start);
}

// ---------------------------------------------------------------------------------------------------------------------

void OrderedSection::setLength(size_t l) { length = l; }

// ---------------------------------------------------------------------------------------------------------------------

OrderedSection::~OrderedSection() { lock->finished(length); }

// ---------------------------------------------------------------------------------------------------------------------

}  // namespace util
}  // namespace genie

