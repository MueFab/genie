#ifndef GENIE_BITREADER_IMPL_H
#define GENIE_BITREADER_IMPL_H

// ---------------------------------------------------------------------------------------------------------------------

namespace genie {
namespace util {

// ---------------------------------------------------------------------------------------------------------------------

template <typename T, typename>
T BitReader::read() {
    return static_cast<T>(read_b(sizeof(T) * 8));
}

// ---------------------------------------------------------------------------------------------------------------------

template <typename T, typename>
T BitReader::read(uint8_t s) {
    return static_cast<T>(read_b(s));
}

// ---------------------------------------------------------------------------------------------------------------------

}  // namespace util
}  // namespace genie

// ---------------------------------------------------------------------------------------------------------------------

#endif  // GENIE_BITREADER_IMPL_H

